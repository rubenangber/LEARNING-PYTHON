#   RATIONAL OPERATORS

"""
Returns True or False

Equal --> ==
Distinct --> !=
Greater than --> >
Less than --> <
Greater or equal --> >=
Less or equal --> <=
"""

num1 = 45
num2 = 10
condition1 = (num1 == num2)
condition2 = (num1 != num2)
condition3 = (num1 > num2)

print(condition1)
print(condition2)
print(condition3)