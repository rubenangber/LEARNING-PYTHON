"""
Concatenate
str + str
"""
result = "This" + " is" + " my" + " home"
print(result)

"""
Multiply
str * int
"""
result = "Home" * 3
print(result)

"""
Operator in
"""
result = "coffee" in "A cup of coffee"
print(result)
result = "o" in "coffee"
print(result)

"""
Comparison
"""
result = "egg" == "Egg"
print(result)
result = "Egg" == "Egg"
print(result)
result = "egg" != "Egg"
print(result)
result = "Egg" != "Egg"
print(result)

"""
Python order:
ABCDEFGHI...abcdefghi...
Where A is lower than a
"""
result = "Home" > "Egg"
print(result)
result = "Egg" > "Home"
print(result)

"""
Quotes
"""
result = "This is how you put \"quotes\""
print(result)